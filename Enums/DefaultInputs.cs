namespace Box.Enums;

/// <summary>
/// Represents the default input mappings. If the player doesn't create their own input map, this set is used.
/// </summary>
public enum DefaultInputs
{
    /// <summary>
    /// Move left input action.
    /// </summary>
    MoveLeft,

    /// <summary>
    /// Move right input action.
    /// </summary>
    MoveRight,

    /// <summary>
    /// Move up input action.
    /// </summary>
    MoveUp,

    /// <summary>
    /// Move down input action.
    /// </summary>
    MoveDown,

    /// <summary>
    /// Accept input action.
    /// </summary>
    Accept,

    /// <summary>
    /// Cancel input action.
    /// </summary>
    Cancel,

    /// <summary>
    /// Other unspecified input action.
    /// </summary>
    Other,
}
