namespace Box.Enums;

/// <summary>
/// Represents the state of a sound.
/// </summary>
public enum SoundState
{
    /// <summary>
    /// The sound is not currently playing.
    /// </summary>
    Stopped,

    /// <summary>
    /// The sound is currently paused.
    /// </summary>
    Paused,

    /// <summary>
    /// The sound is currently playing.
    /// </summary>
    Playing
}
